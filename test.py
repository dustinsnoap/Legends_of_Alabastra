def decompress_tileset(compressed_tileset):
    tileset = []
    tile = []
    for char in compressed_tileset:
        binary = str(bin(ord(char)))[2:]
        binary = [int(x) for x in list('0'*(16-len(binary))+binary)]
        tile.append(binary)
        if len(tile) == 8:
            tileset.append(tile)
            tile = []
    return tileset

def compress_tile(tile):
    compressed = ''
    for i in range(0,8):
        row = tile[i]
        row = ''.join([str(c) for c in row])
        row = int(row, 2)
        compressed = compressed + chr(row)
    return compressed

def compress_tileset(tileset):
    compressed = ''
    for tile in tileset:
        compressed = compressed + compress_tile(tile)
    return compressed


